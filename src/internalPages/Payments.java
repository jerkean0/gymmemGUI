    private void searchUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchUserMouseClicked
        config.configclass conf = new config.configclass();
    
    // 1. Get the text from your search field (search3 in your code)
    String txt = search3.getText();
    
    // 2. Create the SQL query using your database column names
    // We use LIKE and % symbols to find partial matches
    String query = "SELECT * FROM payments WHERE m_id LIKE '%" + txt + "%' "
                 + "OR p_member_id LIKE '%" + txt + "%' "
                 + "OR p_status LIKE '%" + txt + "%' "
                 + "OR p_category LIKE '%" + txt + "%'";

    // 3. Update the table with the filtered results
    conf.displayData(query, payment_table);
    }//GEN-LAST:event_searchUserMouseClicked

