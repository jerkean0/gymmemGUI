    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
      int rowIndex = payment_table.getSelectedRow();
    
    if (rowIndex < 0) {
        javax.swing.JOptionPane.showMessageDialog(null, "Please select a row to delete!");
    } else {
        TableModel model = payment_table.getModel();
        // Column 0 is the ID column in your table
        String id = model.getValueAt(rowIndex, 0).toString();
        
        int confirm = javax.swing.JOptionPane.showConfirmDialog(null, 
                "Are you sure you want to delete Payment ID: " + id + "?", "Warning", 
                javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            config.configclass conf = new config.configclass();
            // IMPORTANT: Changed 'm_id' to 'p_id' to match your database
            String query = "DELETE FROM payments WHERE p_id = '" + id + "'";
            conf.deleteData(query);
            displayData(); // This refreshes the table
        }
    }
    }//GEN-LAST:event_deleteMouseClicked

