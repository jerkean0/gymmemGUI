    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        int rowIndex = payment_table.getSelectedRow(); 

    if (rowIndex < 0) {
        javax.swing.JOptionPane.showMessageDialog(null, "Please select an item to update!");
    } else {
        try {
            TableModel model = payment_table.getModel();
            paymentsForm pf = new paymentsForm();

            // IMPORTANT: Store the Primary Key (m_id) in the 'Name' property for the WHERE clause
            pf.m_id.setName(model.getValueAt(rowIndex, 0).toString());

            // Transfer data from table to Form fields
            pf.m_id.setText(model.getValueAt(rowIndex, 1).toString());      // Member ID
            pf.ammount1.setText(model.getValueAt(rowIndex, 2).toString());  // Amount
            pf.jComboBox1.setSelectedItem(model.getValueAt(rowIndex, 3).toString()); // Category
            pf.ammount.setText(model.getValueAt(rowIndex, 5).toString());   // Cashier name

            // Change UI to Update Mode
            pf.jLabel1.setText("UPDATE PAYMENT");
            pf.jLabel7.setText("UPDATE");

            // Display the form inside the DesktopPane
            this.getDesktopPane().add(pf);
            pf.setVisible(true);

        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_updateMouseClicked

